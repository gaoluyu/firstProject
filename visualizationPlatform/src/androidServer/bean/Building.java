package androidServer.bean;

public class Building {
	private int id;
	private String province;
	private String city;
	private String region;
	private String unit;
	private int floorUpGround;
	private int floorUnderGround;
	private String name;
	private int ci;
	private String network;

	public String getNetwork() {
		return network;
	}

	public void setNetwork(String network) {
		this.network = network;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public int getFloorUpGround() {
		return floorUpGround;
	}

	public void setFloorUpGround(int floorUpGround) {
		this.floorUpGround = floorUpGround;
	}

	public int getFloorUnderGround() {
		return floorUnderGround;
	}

	public void setFloorUnderGround(int floorUnderGround) {
		this.floorUnderGround = floorUnderGround;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCi() {
		return ci;
	}

	public void setCi(int ci) {
		this.ci = ci;
	}

}
